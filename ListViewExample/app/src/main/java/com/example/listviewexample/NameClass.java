package com.example.listviewexample;

public class NameClass {
    private String first;
    private String second;
    public NameClass(String _first, String _second)
    {
        this.first = _first;
        this.second = _second;
    }

    public String getSecond() {
        return second;
    }

    public String getFirst() {
        return first;
    }
}
